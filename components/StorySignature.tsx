export function StorySignature({ className, svgRef, style }: StorySignatureProps) {
  const localSvgRef = useRef<SVGSVGElement | null>(null);

  useLayoutEffect(() => {
    const svg = localSvgRef.current;

    if (!svg) {
      return;
    }

    const bounds = svg.getBBox();
    const padding = 12;
    svg.setAttribute(
      "viewBox",
      `${bounds.x - padding} ${bounds.y - padding} ${bounds.width + padding * 2} ${bounds.height + padding * 2}`,
    );
  }, []);

  return (
    <svg
      ref={(node) => {
        localSvgRef.current = node;
        svgRef?.(node);
      }}
      className={className}
      style={style}
      viewBox="0 0 429 368"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      aria-hidden="true"
      preserveAspectRatio="xMidYMid meet"
    >
      {SIGNATURE_PATHS.map((path, index) => (
        <path
          key={index}
          d={path}
          stroke="currentColor"
          strokeWidth="4"
          strokeLinecap="round"
        />
      ))}
    </svg>
  );
}
